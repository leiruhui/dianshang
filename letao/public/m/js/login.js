$(function(){
    $('#login-btn').on('tap',function(){
        var username = $.trim($('[name = "username"]').val());
        var password =  $.trim($('[name = "password"]').val());
        if(!username){
            mui.toast('请输入正确的用户名');
            return;
        }
        if(!password){
            mui.toast('请输入正确的密码');
            return;
        }
        $.ajax({
            url:'/user/login',
            type:'POST',
            data:{
                username:username,
                password:password
            },
            //回调函数进行文字体醒
            beforeSend:function(){
                $('#login-btn').html('正在登录...')
            },
            success:function(res){
                mui.toast('登录成功');
                //$('#login-btn').html('登录')
                setTimeout(function(){
                    location.href="user.html";
                },2000)
            }
        })
    })
})